#include "./pcr.h"

int	pcr_vline(t_disp *d, t_pcrparam p)
{
	int	t;

	t = p.y1 - 1;
	while (++t <= p.y2)
		pcr_pixel(d, p.x1, t, p.cr1);
	return (0);
}
